    var currentLang = localStorage.getItem('cc-lang') || 'en';

    function applyLanguage(lang) {
        currentLang = lang;
        localStorage.setItem('cc-lang', lang);

        var t = T[lang] || T.en;
        var isRTL = (lang === 'ar');

        document.documentElement.setAttribute('dir', isRTL ? 'rtl' : 'ltr');
        document.documentElement.setAttribute('lang', lang);
        document.documentElement.classList.toggle('lang-ar', isRTL);

        if (isRTL) {
            if (!document.getElementById('cc-arabic-font')) {
                var link = document.createElement('link');
                link.id = 'cc-arabic-font';
                link.rel = 'stylesheet';
                link.href = 'https://fonts.googleapis.com/css2?family=Tajawal:wght@400;500;700;800&display=swap';
                document.head.appendChild(link);
            }
        }

        document.querySelectorAll('[data-i18n]').forEach(function (el) {
            var key = el.getAttribute('data-i18n');
            if (t[key] !== undefined) el.textContent = t[key];
        });

        document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
            var key = el.getAttribute('data-i18n-html');
            if (t[key] !== undefined) el.innerHTML = t[key];
        });

        document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
            var key = el.getAttribute('data-i18n-placeholder');
            if (t[key] !== undefined) el.placeholder = t[key];
        });

        document.querySelectorAll('.lang-btn').forEach(function (btn) {
            var isActive = btn.getAttribute('data-lang') === lang;
            btn.classList.toggle('lang-btn-active', isActive);
        });

        if (window.CC_I18N && window.CC_I18N.hooks) {
            window.CC_I18N.hooks.forEach(function (hook) { hook(lang); });
        }
    }

    window.CC_I18N = { apply: applyLanguage, current: function () { return currentLang; }, hooks: [] };

    function domReady(fn) {
        if (document.readyState !== 'loading') {
            fn();
        } else {
            document.addEventListener('DOMContentLoaded', fn);
        }
    }

    domReady(function () {
        document.querySelectorAll('.lang-btn').forEach(function (btn) {
            btn.addEventListener('click', function () {
                applyLanguage(btn.getAttribute('data-lang'));
            });
        });
        if (currentLang === 'ar') {
            applyLanguage('ar');
        } else {
            document.querySelectorAll('.lang-btn').forEach(function (btn) {
                btn.classList.toggle('lang-btn-active', btn.getAttribute('data-lang') === 'en');
            });
        }
    });
