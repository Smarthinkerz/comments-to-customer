        c.pc_h4s = [].map.call(modal.querySelectorAll('.modal-pricing-card h4'), function (e) { return e.textContent; });
        c.pc_lis = [].map.call(modal.querySelectorAll('.modal-pricing-card li'), function (e) { var i = e.querySelector('i'); return i ? e.textContent.replace(i.textContent, '').trim() : e.textContent.trim(); });
        c.check_items = [].map.call(modal.querySelectorAll('.modal-check-list li, .modal-cross-list li'), function (e) { var i = e.querySelector('i'); return i ? e.textContent.replace(i.textContent, '').trim() : e.textContent.trim(); });
        c.sitemap_lis = [].map.call(modal.querySelectorAll('.sitemap-list li'), function (e) { return e.textContent.trim(); });
