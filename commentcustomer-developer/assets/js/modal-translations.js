        c.h2 = h2 ? h2.textContent : null;
        var intro = modal.querySelector('.modal-intro');
        c.intro = intro ? intro.textContent : null;
        c.h3s = [].map.call(modal.querySelectorAll('.cc-modal-body h3'), function (e) { return e.textContent; });
        c.fi_h4s = [].map.call(modal.querySelectorAll('.modal-feature-item h4'), function (e) { return e.textContent; });
        c.fi_ps = [].map.call([].filter.call(modal.querySelectorAll('.modal-feature-item p'), filterP), function (e) { return e.textContent; });
